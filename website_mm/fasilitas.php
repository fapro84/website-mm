<div class="post_text_area" itemprop="articleBody"><h3>&gt; Ruang Kelas</h3>
<p>Ruang kelas dilengkapi pendingin ruangan (AC) dan didesain agar anak merasa nyaman dan dapat berinteraksi dengan guru serta teman-temannya dengan leluasa. Selain meja-kursi, disediakan karpet untuk kegiatan circle time dan pembelajaran di dalam kelas. Letak meja dan kursi dapat dirubah sesuai dengan kebutuhan pembelajaran. Masing-masing kelas dilengkapi dengan Library Corner untuk menunjang program membaca siswa.</p>
<h3>&gt; Laboratorium Komputer</h3>
<p>Laboratorium computer SSH dilengkapi dengan sejumlah computer atau PC Quad Core dengan jaringan internet yang jumlahnya setara dengan jumlah siswa per kelasnya dengan ruangan ber-AC.</p>
<h3>&gt; Laboratorium IPA</h3>
<p>Laboratorium IPA dilengkapi dengan sejumlah alat praktikum Science yang jumlahnya setara dengan jumlah siswa per kelasnya dengan ruangan ber-AC.</p>
<h3>&gt;Ruang Kesehatan</h3>
<p>Demi mengantisipasi adanya siswa yang kurang sehat saat di sekolah, ruang kesehatan dilengkapi dengan obat-obatan, P3K, ranjang dan ruangan ber-AC.</p>
<h3>&gt; Kantin Sekolah</h3>
<p>Kantin sekolah Smart School menyediakan makanan dan minuman ringan yang sudah terjamin ke-halal-annya dan aman dikonsumsi untuk para siswa.</p>
<h3>&gt; Perpustakaan &amp; Audio visual</h3>
<p>Perpustakaan digunakan dalam pembelajaran bahasa, social dan lainnya. Dalam perpustakaan dikondisikan kegiatan membaca, mencari sumber belajar maupun tugas mandiri. Koleksi buku dan referensi disediakan dengan memperhitungkan kebutuhan sumber belajar anak di sekolah.</p>
<p>Sekolah memiliki perpustakaan (Library Media Center) yang dipadukan dengan ruang kreativitas siswa. Jenis koleksi yang tersedia bervariasi antara lain berupa buku-buku pengetahuan, fiksi, referensi, ensiklopedia, majalah dan surat kabar. Disamping itu tersedia pula koleksi yang berupa CD pembelajaran dan video untuk pengembangan proses belajar siswa di kelas, serta permainan edukatif seperti puzzle, balok, logico dan sebagainya.</p>
<h3>&gt; Mushola</h3>
<p>Mushola berada di dalam area sekolah. Untuk mendukung pelayanan terhadap jamaah, mushola dilengkapi dengan peralatan sound system yang memadai, yang sekaligus merupakan tempat aktifitas keagamaan.</p>
<h3>&gt; Lapangan Olahraga</h3>
<p>Lapangan olahraga telah didesain untuk beragam pelajaran olahraga seperti bola basket, footsal, volley dan atletik. Lapangan yang luas dan sarana olahraga yang beragam memberi peluang lebih besar untuk dapat mengembangkan motorik kasar dan bakat anak. Lapangan dan sarana olahraga yang ada terdiri dari basket, badminton dan futsal.</p>
<h3>&gt; Play Ground</h3>
<p>Demi memastikan para siswa bermain dengan aman dan dapat mengeksplore kemampuan motorik secara baik. Sekolah menyediakan taman bermain khusus untuk para siswa.</p>
<h3>&gt; Keamanan</h3>
<p>Berada dalam lingkungan yang asri dan nyaman, sekolah ini dilengkapi oleh personil keamanan yang professional (Satpam). Dengan kelengkapan keamanan tersebut, mampu menciptakan keamanan dan kenyamanan kegiatan pembelajaran maupun kenyamanan lingkungan di sekitar sekolah.</p>

				
